List = 1, 2, 3

#Reverses the list
list.reverse()
print(List)

#Inserting at any position 
list.insert(2,3)
print(list)

#deleting any value 
list.remove(4)
print(list)

#Add element to end
list.append(4)
print(list)

#Delet any index
list.pop(3)
print(list)

#Sort in ascending order
list.sort()
print(list)

#Sort in descending order
list.sort(reverse=True)
print(list)